/**
 * Embed backend/*.py into a TypeScript module so community installs
 * (main.js only) still carry the Python sync runtime.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const backend = path.join(root, "backend");
const outFile = path.join(root, "src", "backendBundle.generated.ts");

const FILES = [
  "sync.py",
  "login.py",
  "config.py",
  "zoom_notes.py",
  "index_store.py",
  "lockfile.py",
  "procs.py",
  "security.py",
  "requirements.txt",
  "scripts/register-task.ps1",
  "scripts/check.ps1",
];

const bundle = {};
for (const rel of FILES) {
  const full = path.join(backend, rel);
  if (!fs.existsSync(full)) {
    console.error(`Missing backend file: ${rel}`);
    process.exit(1);
  }
  bundle[rel] = fs.readFileSync(full, "utf8");
}

const body =
  `/* AUTO-GENERATED by scripts/bundle-backend.mjs — do not edit */\n` +
  `export const BACKEND_FILES: Record<string, string> = ${JSON.stringify(
    bundle,
    null,
    2
  )};\n`;

fs.writeFileSync(outFile, body, "utf8");
console.log(
  `Wrote ${outFile} (${FILES.length} files, ${Buffer.byteLength(body)} bytes)`
);
